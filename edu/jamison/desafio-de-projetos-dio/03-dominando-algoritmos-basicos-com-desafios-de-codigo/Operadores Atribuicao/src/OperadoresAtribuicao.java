
public class OperadoresAtribuicao {

	public static void main(String[] args) {

		int numero = 5;
		
		// x repeticao
		
		numero++;
		// mesma coisa que botar numero = numero + 1
		
		System.out.println(numero);		
		
		// Para fazer uma negação de uma expressão boleana, usamos o sinal !
		
		boolean variavel = false;
		
		// Mas essa variável abaixo, é uma memória, se impremirmos novamente a variavel, teremos o resultado original 
		System.out.println(!variavel);		
		
		// Exemplo do comentário acima
		System.out.println(variavel);
		
		
	}

}
